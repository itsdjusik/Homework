def add(x, y):
    """Функция возвращает сумму двух чисел"""
    return x + y


def subtract(x, y):
    """Функция возвращает разность двух чисел """
    return x - y


def multiply(x, y):
    """Функция возвращает выражение двух чисел"""
    return x * y


def divide(x, y):
    """Функция делит два числа друг на друга"""
    return x / y
